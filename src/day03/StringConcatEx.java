package day03;

public class StringConcatEx {
    public static void main(String[] args) {
        int result = 10 + 2 + 8;
        System.out.println(result);

        String result1 = 10 + "2" + 8;
        System.out.println(result1);

        String result2 = 10 + 8 + "2";
        System.out.println(result2);
    }
}
